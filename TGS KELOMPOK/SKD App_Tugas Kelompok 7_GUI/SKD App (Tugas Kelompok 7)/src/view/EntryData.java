    // method utk muat data dari database
    public void loadTableData() {
        DefaultTableModel dtm = (DefaultTableModel) tabel_data.getModel();
        
        // refresh tabel
        while(dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        Connector connector = new Connector();
        try (Connection connection = connector.getConnection()) {
            String sql = "SELECT * FROM keterangan_pencacah";
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery(sql);
            
            int no = 1;
            
            while (resultSet.next()) {
                String noResponden = resultSet.getString("no_responden");
                String namaPemeriksa = resultSet.getString("nama_pemeriksa");
                String namaPencacah = resultSet.getString("nama_pencacah");
                Date tanggalInput = resultSet.getDate("tanggal");
                
                dtm.addRow(new Object[]{no++, namaPemeriksa, namaPencacah, noResponden, tanggalInput});
            }
        }
        catch (SQLException ex) {
            System.err.println(ex);
            JOptionPane.showMessageDialog(this, "Gagal mengambil data", "Gagal", JOptionPane.ERROR_MESSAGE);
        }
    }
    
