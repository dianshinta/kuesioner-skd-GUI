    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        int selectedRow = tabel_data.getSelectedRow();
        if (selectedRow != -1) {
            // ambil no_responden dari tabel
            Object noRespondenObj = tabel_data.getValueAt(selectedRow, 3);
            String noResponden = noRespondenObj.toString(); // konversi ke string
            
            InputKetKuesioner inputKuesionerForm = new InputKetKuesioner(this, noResponden);
            inputKuesionerForm.getValueField(noResponden); // dapatkan value untuk field kuesioner
            inputKuesionerForm.setVisible(true);
            this.dispose();
        }
        else {
            JOptionPane.showMessageDialog(this, "Mohon pilih salah satu baris untuk mengedit data.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabel_data.getSelectedRow();
        if (selectedRow != -1) { // jika ada baris yang terpilih
            // ambil no_responden dari tabel
            Object noRespondenObj = tabel_data.getValueAt(selectedRow, 3);
            String noResponden = noRespondenObj.toString(); // konversi ke string

            int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                Connector connector = new Connector();
                try (Connection connection = connector.getConnection()) {
                    // hapus data dari tabel keterangan_responden
                    String sqlDeleteResponden = "DELETE FROM keterangan_responden WHERE no_responden = ?";
                    try (PreparedStatement pstmtDeleteResponden = connection.prepareStatement(sqlDeleteResponden)) {
                        pstmtDeleteResponden.setString(1, noResponden);
                        pstmtDeleteResponden.executeUpdate();
                    }

                    // hapus data dari tabel keterangan_pencacah
                    String sqlDeletePencacah = "DELETE FROM keterangan_pencacah WHERE no_responden = ?";
                    try (PreparedStatement pstmtDeletePencacah = connection.prepareStatement(sqlDeletePencacah)) {
                        pstmtDeletePencacah.setString(1, noResponden);
                        pstmtDeletePencacah.executeUpdate();
                    }

                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                    loadTableData(); // muat ulang data di tabel setelah penghapusan
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
                }
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Mohon pilih salah satu baris untuk menghapus data.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

