    private void updateData() {
        if (noResponden != null && !noResponden.trim().isEmpty()) {
            String namaPencacah = inputKetKuesioner.getNamaPencacah();
            String namaPemeriksa = inputKetKuesioner.getNamaPemeriksa();
            java.util.Date tanggalInputUtil = inputKetKuesioner.getTanggalInput();

            if (tanggalInputUtil != null) {
                java.sql.Date tanggalInput = new java.sql.Date(tanggalInputUtil.getTime());
                Connector connector = new Connector();
                try (Connection connection = connector.getConnection()) {
                    String sqlUpdate = "UPDATE keterangan_pencacah SET nama_pencacah = ?, nama_pemeriksa = ?, tanggal = ? WHERE no_responden = ?";
                    try (PreparedStatement pstmtUpdate = connection.prepareStatement(sqlUpdate)) {
                        pstmtUpdate.setString(1, namaPencacah);
                        pstmtUpdate.setString(2, namaPemeriksa);
                        pstmtUpdate.setDate(3, tanggalInput);
                        pstmtUpdate.setString(4, noResponden);
                        pstmtUpdate.executeUpdate();
                    }
                    updateRespondenData(connection);
                    JOptionPane.showMessageDialog(this, "Data Keterangan Kuesioner Berhasil Diupdate!");
                    entryData.loadTableData();
                    clearForm();
                } catch (SQLException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Gagal menyimpan data", "Gagal", JOptionPane.ERROR_MESSAGE);
                } finally {
                    connector.closeConnection();
                }
            }
        }
    }
    
