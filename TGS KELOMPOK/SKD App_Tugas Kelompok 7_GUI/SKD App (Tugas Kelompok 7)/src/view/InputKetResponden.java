    public void getValueField() {
        Connector connector = new Connector();
        try (Connection connection = connector.getConnection()) {
            String sql = "SELECT * FROM keterangan_responden WHERE no_responden = ?";
            try (PreparedStatement pstmt = connection.prepareStatement(sql)) {
                pstmt.setString(1, noResponden);
                try (ResultSet rs = pstmt.executeQuery()) {
                    if (rs.next()) {
                        jTextField1.setText(rs.getString("nama"));
                        jTextField4.setText(rs.getString("tahun_lahir"));
                        jTextField5.setText(rs.getString("email"));
                        jTextField7.setText(rs.getString("no_hp"));
                        if ("Laki-Laki".equals(rs.getString("jenis_kelamin"))) {
                            jRadioButton7.setSelected(true);
                        } else {
                            jRadioButton5.setSelected(true);
                        }
                        jComboBox2.setSelectedItem(rs.getString("pendidikan_tertinggi"));
                        jComboBox3.setSelectedItem(rs.getString("pekerjaan"));
                        jTextField6.setText(rs.getString("instansi"));
                        jComboBox4.setSelectedItem(rs.getString("kat_instansi"));
                        jComboBox5.setSelectedItem(rs.getString("pemanfaatan"));
                        String[] jenisLayananArray = rs.getString("jenis_layanan").split(",");
                        for (String layanan : jenisLayananArray) {
                            switch (layanan) {
                                case "Perpustakaan":
                                    jCheckBox1.setSelected(true);
                                    break;
                                case "Pembelian Publikasi BPS":
                                    jCheckBox2.setSelected(true);
                                    break;
                                case "Pembelian Data Mikro/Peta Wilayah Kerja Statistik":
                                    jCheckBox3.setSelected(true);
                                    break;
                                case "Akses Produk Statistik pada Website BPS":
                                    jCheckBox4.setSelected(true);
                                    break;
                                case "Konsultasi Statistik":
                                    jCheckBox6.setSelected(true);
                                    break;
                                case "Rekomendasi Kegiatan Statistik":
                                    jCheckBox5.setSelected(true);
                                    break;
                            }
                        }
                        String[] fasilitasArray = rs.getString("fasilitas").split(",");
                        for (String fasilitas : fasilitasArray) {
                            switch (fasilitas) {
                                case "Datang Langsung ke Unit Pelayanan Statistik terpadu(PST)":
                                    jCheckBox7.setSelected(true);
                                    break;
                                case "Aplikasi Pelayanan Statistik Terpadu Online(pst.bps.go.id)":
                                    jCheckBox8.setSelected(true);
                                    break;
                                case "Website BPS(bps.go.id)":
                                    jCheckBox9.setSelected(true);
                                    break;
                                case "Aplikasi AllStats BPS(berbasis android dan ios)":
                                    jCheckBox10.setSelected(true);
                                    break;
                                case "Lainnya":
                                    jCheckBox11.setSelected(true);
                                    break;
                            }
                        }
                        if ("Ya".equals(rs.getString("rujukan_utama"))) {
                            jRadioButton8.setSelected(true);
                        } else {
                            jRadioButton9.setSelected(true);
                        }
                        if ("Ya".equals(rs.getString("pengaduan"))) {
                            jRadioButton2.setSelected(true);
                        } else {
                            jRadioButton3.setSelected(true);
                        }
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal mengambil data", "Gagal", JOptionPane.ERROR_MESSAGE);
        } finally {
            connector.closeConnection();
        }
    }
    
