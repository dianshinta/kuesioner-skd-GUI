    private void loadData() {
        Connector connector = new Connector();
        String query = "SELECT * FROM keterangan_responden WHERE no_responden = ?";

        try (PreparedStatement pstmt = connector.koneksi.prepareStatement(query)) {
            // Set parameter for nomorResponden
            pstmt.setString(1, noResponden);

            // Execute query
            ResultSet rs = pstmt.executeQuery();

            int no = 1;

            while (rs.next()) {
                // Retrieve data from ResultSet
                String nama = rs.getString("nama");
                String tahunLahir = rs.getString("tahun_lahir");
                String email = rs.getString("email");
                String noHp = rs.getString("no_hp");
                String jenisKelamin = rs.getString("jenis_kelamin");
                String pendidikanTertinggi = rs.getString("pendidikan_tertinggi");
                String pekerjaan = rs.getString("pekerjaan");
                String instansi = rs.getString("instansi");
                String katInstansi = rs.getString("kat_instansi");
                String pemanfaatan = rs.getString("pemanfaatan");
                String jenisLayanan = rs.getString("jenis_layanan");
                String fasilitas = rs.getString("fasilitas");
                String rujukanUtama = rs.getString("rujukan_utama");
                String pengaduan = rs.getString("pengaduan");

                SwingUtilities.invokeLater(() -> {
                    namaField.setText(nama);                    
                    tahunLahirField.setText(tahunLahir);
                    emailField.setText(email);
                    noHpField.setText(noHp);
                    
                    // Set radio buttons for rujukanUtama
                    if ("Laki-Laki".equals(jenisKelamin)) {
                        lakiRadioButton.setSelected(true);
                    } else if ("Perempuan".equals(jenisKelamin)) {
                        perempuanRadioButton.setSelected(true);
                    }
                    
                    // Set combo boxes
                    pendidikanComboBox.setSelectedItem(pendidikanTertinggi);
                    pekerjaanComboBox.setSelectedItem(pekerjaan);
                    instansiField.setText(instansi);
                    katInstansiComboBox.setSelectedItem(katInstansi);
                    pemanfaatanComboBox.setSelectedItem(pemanfaatan);

                    // Set text field for instansi
                    instansiField.setText(instansi);

                    // Set checkboxes for jenisLayanan
                    setCheckboxes(jenisLayananCheckBoxes, jenisLayanan);

                    // Set checkboxes for fasilitas
                    setCheckboxes(fasilitasCheckBoxes, fasilitas);

                    // Set radio buttons for rujukanUtama
                    if ("Ya".equals(rujukanUtama)) {
                        rujukanUtamaYa.setSelected(true);
                    } else if ("Tidak".equals(rujukanUtama)) {
                        rujukanUtamaTidak.setSelected(true);
                    }

                    // Set radio buttons for pengaduan
                    if ("Ya".equals(pengaduan)) {
                        pengaduanYa.setSelected(true);
                    } else if ("Tidak".equals(pengaduan)) {
                        pengaduanTidak.setSelected(true);
                    }
                    
                });
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    private void setCheckboxes(JCheckBox[] checkBoxes, String values) {
        String[] valueArray = values.split(","); // Assuming the values are comma-separated
        for (JCheckBox checkBox : checkBoxes) {
            for (String value : valueArray) {
                if (checkBox.getText().equalsIgnoreCase(value.trim())) {
                    checkBox.setSelected(true);
                    break;
                }
            }
            checkBox.setEnabled(false);
        }
    }
    
